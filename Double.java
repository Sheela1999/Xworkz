public class Double{
	public static void main(String[] args){
		
		//Double datatype
		double b = 4.67;
		double c = 6.8975;
		double d = 69.7543;
		double e = 67.985;
		double f = 0.65432;
		
		
		//output of Double datatype
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}
}